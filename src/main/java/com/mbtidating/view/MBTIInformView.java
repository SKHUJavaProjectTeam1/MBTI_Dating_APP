    private static void loadIcon(String type) {
        String path = "/images/mbti/" + type + ".png";
        java.net.URL url = MBTIInformView.class.getResource(path);
        if (url == null) return;

        Image img = new ImageIcon(url).getImage()
                .getScaledInstance(70, 70, Image.SCALE_SMOOTH);
        ICONS.put(type, new ImageIcon(img));
    }


