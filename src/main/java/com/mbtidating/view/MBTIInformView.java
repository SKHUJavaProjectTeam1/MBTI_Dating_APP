    private JToggleButton makeTypeButton(String type) {
        JToggleButton b = new JToggleButton(type);

        b.setFocusPainted(false);
        b.setOpaque(true);

        // ğŸ”¤ í°íŠ¸ ë³€ê²½
        b.setFont(new Font(MAIN_FONT, Font.BOLD, 18));

        b.setForeground(new Color(70, 55, 120)); // ì§„í•œ ë³´ë¼ í…ìŠ¤íŠ¸

        // ğŸ”² í…Œë‘ë¦¬ ë¶€ë“œëŸ½ê²Œ
        b.setBorder(new CompoundBorder(
                new LineBorder(new Color(220, 210, 240), 1, true),
                new EmptyBorder(12, 8, 12, 8)
        ));

        b.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));

        b.addActionListener(e -> showType(type));

        return b;
    }


    private void showType(String type) {
        MbtiDetail d = INFO.get(type);
        if (d == null) return;

        bigType.setText(type);
        desc.setText(d.title + "\n\n" + d.desc);   // ì„¤ëª… í…ìŠ¤íŠ¸ í‘œì‹œ
        desc.setCaretPosition(0);
    }

    
